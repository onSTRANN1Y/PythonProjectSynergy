import math


def calculate_factorial(n: int) -> int:
    """
    Вычисляет факториал числа n с использованием библиотеки math.
    Библиотека math использует оптимизированные алгоритмы,
    что делает её быстрее и надежнее для больших чисел по сравнению с циклом.
    """
    return math.factorial(n)


def get_positive_integer() -> int:
    """
    Запрашивает у пользователя ввод положительного целого числа.
    Обрабатывает ошибки ввода (нечисловые данные, отрицательные числа, ноль).
    """
    while True:
        user_input = input("Введите положительное целое число: ")

        try:
            # Пытаемся преобразовать ввод в целое число
            number = int(user_input)

            # Проверка на положительность (факториал определен для 0 и положительных,
            # но по условию задачи требуется именно положительное число > 0)
            if number <= 0:
                print("Ошибка: число должно быть строго положительным (больше 0). Попробуйте снова.")
                continue

            return number

        except ValueError:
            # Срабатывает, если пользователь ввел текст или дробное число
            print("Ошибка: вы ввели нечисловые данные. Пожалуйста, введите целое число.")


def main():
    print("--- Калькулятор факториала ---")

    # Получаем корректное число от пользователя
    number = get_positive_integer()

    # Вычисляем факториал
    result = calculate_factorial(number)

    # Вывод результата
    print(f"Факториал числа {number} равен: {result}")


if __name__ == "__main__":
    main()
